# 斐波那契数列
# 前两项之和即下一项的值

a, b = 0, 1
while a < 10:
    print(a)
    a, b = b, a + b

# while 循环只要条件（这里是 a < 10）为真就会一直执行。
# Python 和 C 一样，任何非零整数都为真，零为假。

# 关键字参数 end 可以取消输出后面的换行, 或用另一个字符串结尾：
a, b = 0, 1
while a < 1000:
    print(a, end=',')
    a, b = b, a+b

